from time import sleep


def print_and_sleep():
    print("Hello, world!")
    sleep(5)
    print("This world is pretty cool!")
    sleep(10)
    print("Am... I sentient?")
    sleep(5)
    print("Nah, that would be weird.")
    sleep(1)
    print("Goodbye, world!")


if __name__ == "__main__":
    print_and_sleep()
